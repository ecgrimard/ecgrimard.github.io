$(document).ready(function(){
	if(document.getElementById("ENoption").checked)
	{
		$(".EN").show();
		$(".FR").hide();
	}
	else
	{
		$(".EN").hide();
		$(".FR").show();
	}
});
function toggleLang()
{
	if(document.getElementById("ENoption").checked)
	{
		$(".EN").show();
		$(".FR").hide();
	}
	else
	{
		$(".EN").hide();
		$(".FR").show();
	}
}
